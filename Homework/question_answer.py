# What is class, object, and instance?
# A class is a type where objects are stored as instances. An object stores data using variables that they
# already have, and are the main way for Python to represent data. An instance is like an object, except it would be better to use an instance when you are using the built-in function class.
# What is class variable and instance variable?
# Class variables are variables that belong on the same level as the class, while instance variables are
# variables that belong on the same levels as objects or instances, inside of the class.
# What is inheritance?
# Inheritance is the parts that you pass on, from a superclass to a subclass.
